WITH PlayerAges AS (
    SELECT
        player_ID,
        player_name,
        player_last_name,
        team_ID,
        TIMESTAMPDIFF(YEAR, birthdate, CURDATE()) AS age_years
    FROM
        Player
)

SELECT
    p1.player_ID,
    p1.player_name,
    p1.player_last_name,
    p1.team_ID,
    p1.age_years
FROM
    PlayerAges p1
JOIN
    PlayerAges p2 ON p1.team_ID = p2.team_ID AND p1.age_years = p2.age_years
WHERE
    p1.player_ID <> p2.player_ID
ORDER BY
    p1.team_ID, p1.age_years;
    
